package com.lwb.Study.lambda.day01.day02;

/**
 * 曲目类
 */
public class Track {

    /**
     * 曲目名称
     */
    private String name;

    public Track() {
    }

    public Track(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
